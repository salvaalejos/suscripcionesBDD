    class HiloLogin extends SwingWorker {

        @Override
        protected Object doInBackground() throws Exception {
            while(ejecutaLogin) {
                try {
                    ServerSocket serverSocket = new ServerSocket(
                            LOGIN_PORT
                    );
                    Socket socket = serverSocket.accept();

                    ObjectInputStream ois = new ObjectInputStream(
                            socket.getInputStream()
                    );

                    User usuario = (User) ois.readObject();

                    boolean existe = false;
                    for(User u : usersList) {
                        if(u.getUser().equals(usuario.getUser()) &&
                                u.getPassword().equals(usuario.getPassword())) {
                            usuario = u;
                            existe = true;
                            break;
                        }
                    }

                    publish(
                            existe ? "SI" : "NO",
                            existe ? "Acceso concedido" : "Acceso denegado",
                            usuario
                    );

                    ois.close();
                    socket.close();
                    serverSocket.close();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
            return "";
        }

        @Override
        protected void process(List chunks) {
            bitacoraServidor(chunks);
        }

    }

    /**
     * Hilo que escucha peticiones de registro
     */
    class HiloRegister extends SwingWorker {

        @Override
        protected Object doInBackground() throws Exception {
            while(ejecutaRegister) {
                try {
                    ServerSocket serverSocket = new ServerSocket(
                            REGISTER_PORT
                    );
                    Socket socket = serverSocket.accept();

                    ObjectInputStream ois = new ObjectInputStream(
                            socket.getInputStream()
                    );

                    User usuario = (User) ois.readObject();

                    if(usuario != null) {
                        usersList.add(usuario);
                        escribirUsuarios();

                        publish("SI", "Registrado", usuario);
                    } else {
                        publish("NO", "No se registr√≥", "");
                    }

                    ois.close();
                    socket.close();
                    serverSocket.close();
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
            return "";
        }

        @Override
        protected void process(List chunks) {
            bitacoraServidor(chunks);
        }

    }

